#Boolean
a=True
b=False
print(type(a))
print(type(b))


#Convert to Integer
a=int(10.3)
b=int('20')
print(a,type(a))
print(b,type(b))


#Conver to Float
a=float(9)
b=float('6.3')
print(a,type(a))
print(b,type(b))


#Convert to String
a=str(9)
b=str(5.4)
print(a,type(a))
print(b,type(b))


#Convert Integer to Boolean
a=bool(10)
